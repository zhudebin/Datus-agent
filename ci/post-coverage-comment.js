// Post or update the coverage comment on a PR.
// Called by actions/github-script in the coverage workflow.
// Inputs via environment: steps.metrics.outputs.*

module.exports = async ({ github, context, core }) => {
  const fs = require('fs');

  let diffReport = '';
  try { diffReport = fs.readFileSync('ci/diff-cover-report.md', 'utf8'); } catch {}
  let testReport = '';
  try { testReport = fs.readFileSync('ci/test-report.md', 'utf8'); } catch {}

  const overall = process.env.METRIC_OVERALL || '';
  const diff = process.env.METRIC_DIFF || '';
  const testOutcome = process.env.METRIC_TEST_OUTCOME || '';
  const testTotal = process.env.METRIC_TEST_TOTAL || '0';
  const testPassed = process.env.METRIC_TEST_PASSED || '0';
  const testFailed = process.env.METRIC_TEST_FAILED || '0';
  const testSkipped = process.env.METRIC_TEST_SKIPPED || '0';

  const ok = (v) => parseFloat(v) >= 80 ? '\u2705' : '\u274C';
  const testSummary = `${testPassed}/${testTotal} passed` +
    (parseInt(testFailed) > 0 ? `, ${testFailed} failed` : '') +
    (parseInt(testSkipped) > 0 ? `, ${testSkipped} skipped` : '');

  const hasFails = parseInt(testFailed) > 0;

  const MAX_BODY = 65000; // GitHub comment limit is 65536 chars; leave margin
  const footer = `\n\n---\n*Generated by pytest-cov + diff-cover*`;
  const header = `## \ud83d\udcca PR Test Coverage Report\n\n` +
    `| Metric | Value | Threshold | Status |\n|--------|-------|-----------|--------|\n` +
    `| **Overall Coverage** | ${overall}% | - | \u2139\ufe0f |\n` +
    `| **Diff Coverage** | ${diff}% | 80% | ${ok(diff)} |\n` +
    `| **PR Tests** | ${testSummary} | pass | ${testOutcome === 'success' ? '\u2705' : '\u274C'} |\n\n`;

  const truncNote = '\n\n> **Note:** Report truncated to fit GitHub comment size limit.\n';

  let details =
    `<details><summary>Coverage Details</summary>\n\n${diffReport || 'No diff coverage data.'}\n</details>\n\n` +
    (testReport ? `<details><summary>Test Failure Details${hasFails ? ' (\u274C)' : ''}</summary>\n\n${testReport}\n</details>\n\n` : '');

  let body = header + details + footer;
  if (body.length > MAX_BODY) {
    const available = MAX_BODY - header.length - footer.length - truncNote.length;
    details = details.slice(0, available);
    body = header + details + truncNote + footer;
  }

  const prNumber = context.payload.pull_request?.number;
  if (!prNumber) {
    core.warning('No pull request context found, skipping comment');
    return;
  }

  const { data: comments } = await github.rest.issues.listComments({
    owner: context.repo.owner, repo: context.repo.repo,
    issue_number: prNumber,
  });
  const existing = comments.find(c =>
    c.body?.includes('PR Test Coverage Report') ||
    c.body?.includes('Unit Test Coverage Report')
  );
  const params = { owner: context.repo.owner, repo: context.repo.repo, body };
  if (existing) {
    await github.rest.issues.updateComment({ ...params, comment_id: existing.id });
  } else {
    await github.rest.issues.createComment({ ...params, issue_number: prNumber });
  }
};
